async function main() {
  console.log("🌱 Starting seed...");

  // Get unique emails (remove duplicates)
  const uniqueEmails = [...new Set(emails)];

  console.log(`📧 Total unique emails: ${uniqueEmails.length}`);

  let created = 0;
  let skipped = 0;
  let errors = 0;

  for (const email of uniqueEmails) {
    try {
      // Check if email already exists
      const existing = await prisma.contactEmail.findUnique({
        where: { email },
      });

      if (existing) {
        console.log(`⏭️  Skipped (already exists): ${email}`);
        skipped++;
        continue;
      }

      // Extract name from email (before @)
      const name = email.split("@")[0].replace(/[._-]/g, " ");
      const capitalizedName = name
        .split(" ")
        .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
        .join(" ");

      // Create contact with email
      await prisma.contact.create({
        data: {
          name: capitalizedName,
          marketingConsent: true, // All have consent for marketing
          emails: {
            create: {
              email: email.toLowerCase(),
            },
          },
        },
      });

      console.log(`✅ Created: ${email} (${capitalizedName})`);
      created++;
    } catch (error) {
      console.error(`❌ Error creating ${email}:`, error);
      errors++;
    }
  }

  console.log("\n📊 Seed Summary:");
  console.log(`✅ Created: ${created}`);
  console.log(`⏭️  Skipped: ${skipped}`);
  console.log(`❌ Errors: ${errors}`);
  console.log(`📧 Total: ${uniqueEmails.length}`);
}

main()
  .catch((e) => {
    console.error("❌ Seed failed:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
